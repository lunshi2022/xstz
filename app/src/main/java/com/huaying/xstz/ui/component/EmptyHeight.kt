package com.huaying.xstz.ui.component

import androidx.compose.runtime.Composable
import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/**
 * 参考项目 gkd-main 的设计
 * 提供统一的空高度值，用于布局中
 */
@Composable
fun EmptyHeight(): Dp = 48.dp

/**
 * 标准的垂直间距
 */
val StandardVerticalPadding = 16.dp
val StandardHorizontalPadding = 16.dp

/**
 * 小间距
 */
val SmallVerticalPadding = 8.dp
val SmallHorizontalPadding = 12.dp
